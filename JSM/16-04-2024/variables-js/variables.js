/* 
format pembuatan variable di js :
keywordVar namaVariabel = value;

keywordVar :
1. var
2. let --> lebih disarankan daripada var.
3. const --> konstanta
*/

// let
let nama = "lily";
console.log(nama);
document.write(nama);
nama = "sakura"; // mengubah nilai

//const
