/*
=> Output js :
1. console.log() --> tampilkan ke konsol.
2. document.write() --> tampilkan ke halaman/body html

=> Perbedaan antara console.log() dan document.write()
1. console.log() : 
    a. Bisa digunakan untuk melihat "output" dari code JS di console.  
    b. Bisa digunakan untuk melihat error ketika code JS tidak muncul di dokumen/body html.

2. document.write() :
    a. Bisa digunakan untuk melihat "output" di layar putih web (atau document).
    b. Ketika output tidak muncul, ada kemungkinan error. Lihat error di console.
*/

// document.write()
document.write("Hello World");

// console.log()
console.log("Hello World!");
